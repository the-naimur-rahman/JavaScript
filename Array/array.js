// Array
let fruits = ["Apple", "Orange", "Banana", "Coconut"];

for(let fruit of fruits){
    console.log(fruit);
}
console.log("\n");


// Sort An Array in Alphabetical Order
fruits.sort();

for(let fruit of fruits){
    console.log(fruit);
}
console.log("\n");


// Sort An Array in Reverse
fruits.reverse();

for(let fruit of fruits){
    console.log(fruit);
}
console.log("\n");